/// <summary>
/// Cette énumération définit différents types de pistes audio.
/// Chaque membre de l'énumération représente un type de piste audio, tel que 
/// la musique de base, la musique de l'événement A, et la musique de l'événement B.
/// Chaque type de piste est associé à une valeur entière.
/// #tp4 Elyzabelle
/// </summary>
public enum TypePiste
{
    MusiqueBase = 0, // Représente la musique de base.
    MusiqueEvenA = 1, // Représente la musique de l'événement A.
    MusiqueEvenB = 2  // Représente la musique de l'événement B.
}

